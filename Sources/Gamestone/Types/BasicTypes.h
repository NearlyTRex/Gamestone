#ifndef BASICTYPES_H
#define BASICTYPES_H

// Standard includes
#include <cstddef>

// Namespace: Gamestone
// The Gamestone namespace
namespace Gamestone
{
    // Type: Bool
    // Boolean type
    typedef bool Bool;
    
    // Type: Char
    // Signed character type
    typedef char Char;
    
    // Type: Short
    // Signed short type
    typedef short Short;
    
    // Type: Int
    // Signed integer type
    typedef int Int;
    
    // Type: UChar
    // Unsigned character type
    typedef unsigned char UChar;
    
    // Type: UShort
    // Unsigned short type
    typedef unsigned short UShort;
    
    // Type: UInt
    // Unsigned integer type
    typedef unsigned int UInt;
    
    // Type: Float
    // Floating point type
    typedef float Float;
    
    // Type: Double
    // Double precision floating point type
    typedef double Double;
    
    // Type: SizeType
    // Size type
    typedef std::size_t SizeType;
    
    // Type: Void
    // Void type
    typedef void Void;
    
    // Type: VoidPtr
    // Void pointer type
    typedef void* VoidPtr;
};

#endif
